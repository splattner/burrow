// buildDryRunAuthSecret returns a Secret manifest with a placeholder value so
// that dry-run output is complete. The real secret is randomly generated and
// stored at actual deploy time.
func (c *Config) buildDryRunAuthSecret() *corev1.Secret {
	return &corev1.Secret{
		TypeMeta: metav1.TypeMeta{APIVersion: "v1", Kind: "Secret"},
		ObjectMeta: metav1.ObjectMeta{
			Name:      c.AuthSecretName(),
			Namespace: c.Namespace,
			Labels:    c.commonLabels(),
		},
		StringData: map[string]string{
			authSecretDataKey: "<auto-generated at deploy time>",
		},
	}
}

